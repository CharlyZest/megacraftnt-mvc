            <h3>¿Qué son las cookies?</h3>
            <p>
                Las cookies son pequeños archivos de texto almacenados en su ordenador por su navegador web, a petición de un sitio que está viendo. Esto permite que el sitio que estás viendo a recordar cosas sobre usted, como sus preferencias y la historia o para mantenerlo conectado.        
            </p>
            <p>
                Las cookies se pueden almacenar en el ordenador durante un breve periodo de tiempo (por ejemplo, sólo mientras el navegador está abierto) o por un período prolongado de tiempo, incluso años. Las cookies no establecidos por este sitio no serán accesibles para nosotros.
            </p>
            <h3>Nuestro uso de cookies</h3>
            <p>
                Este sitio utiliza cookies para muchas cosas, incluyendo:
            </p>
