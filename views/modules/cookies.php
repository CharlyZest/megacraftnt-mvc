                <li>Analítica. Esto nos permite determinar cómo se está utilizando el sitio y mejorarlo</li>
                <li>cookies de publicidad (posiblemente de terceras partes). Si este sitio muestra publicidad, las cookies pueden ser fijados por los anunciantes para determinar quién ha visto un anuncio o cosas similares. Estas cookies pueden ser establecidos por terceros, en cuyo caso este sitio no tiene capacidad de leer o escribir estas cookies.</li>
                <li>Otras cookies de terceros para cosas como Facebook o Twitter para compartir. Estas cookies serán generalmente establecido por el tercero independiente, por lo que este sitio no tendrán ninguna posibilidad de acceder a ellos.</li>
            </ul>
